-- -------------------------------------------------------------------------------------------------
-- IMPORTAÇÃO DO BRMODELO

/* Filmes_logico: */

CREATE TABLE Filme (
    Id_Filme INT PRIMARY KEY UNIQUE,
    TituloOriginal VARCHAR(100),
    lancamento DATE,
    duracao INT,
    diretor VARCHAR(100),
    pais VARCHAR(20),
    nota FLOAT,
    TituloNacional VARCHAR(100),
    Orcamento DOUBLE,
    Faturamento DOUBLE
);

CREATE TABLE Ator (
    id_ator INT PRIMARY KEY UNIQUE,
    nome Varchar(100),
    data_nascimento DATE,
    pais_origem VARCHAR(20),
    altura FLOAT,
    genero CHAR
);

CREATE TABLE Assunto (
    id_assunto INT PRIMARY KEY,
    tema VARCHAR(50)
);

CREATE TABLE Genero (
    id_genero INT PRIMARY KEY,
    tipo VARCHAR(20)
);

CREATE TABLE Tem (
    fk_Ator_id_ator INT,
    fk_Filme_Id_Filme INT
);

CREATE TABLE Retrata (
    fk_Filme_Id_Filme INT,
    fk_Assunto_id_assunto INT
);

CREATE TABLE Ser (
    fk_Genero_id_genero INT,
    fk_Filme_Id_Filme INT
);
 
ALTER TABLE Tem ADD CONSTRAINT FK_Tem_1
    FOREIGN KEY (fk_Ator_id_ator)
    REFERENCES Ator (id_ator)
    ON DELETE RESTRICT;
 
ALTER TABLE Tem ADD CONSTRAINT FK_Tem_2
    FOREIGN KEY (fk_Filme_Id_Filme)
    REFERENCES Filme (Id_Filme)
    ON DELETE RESTRICT;
 
ALTER TABLE Retrata ADD CONSTRAINT FK_Retrata_1
    FOREIGN KEY (fk_Filme_Id_Filme)
    REFERENCES Filme (Id_Filme)
    ON DELETE RESTRICT;
 
ALTER TABLE Retrata ADD CONSTRAINT FK_Retrata_2
    FOREIGN KEY (fk_Assunto_id_assunto)
    REFERENCES Assunto (id_assunto)
    ON DELETE SET NULL;
 
ALTER TABLE Ser ADD CONSTRAINT FK_Ser_1
    FOREIGN KEY (fk_Genero_id_genero)
    REFERENCES Genero (id_genero)
    ON DELETE RESTRICT;
 
ALTER TABLE Ser ADD CONSTRAINT FK_Ser_2
    FOREIGN KEY (fk_Filme_Id_Filme)
    REFERENCES Filme (Id_Filme)
    ON DELETE SET NULL;

